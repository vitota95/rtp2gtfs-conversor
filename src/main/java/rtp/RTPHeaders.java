public class RTPHeaders {
    String versio;
    String operador;
    String parada;
    String linia;
    String expedicio;
    String restriccio;
    String periode;
    String itinerari;
    String vehicle;
    String temps_itinerari;
    String trajecte;

    public String getVersio() {
        return versio;
    }

    public void setVersio(String versio) {
        this.versio = versio;
    }

    public String getOperador() {
        return operador;
    }

    public void setOperador(String operador) {
        this.operador = operador;
    }

    public String getParada() {
        return parada;
    }

    public void setParada(String parada) {
        this.parada = parada;
    }

    public String getLinia() {
        return linia;
    }

    public void setLinia(String linia) {
        this.linia = linia;
    }

    public String getExpedicio() {
        return expedicio;
    }

    public void setExpedicio(String expedicio) {
        this.expedicio = expedicio;
    }

    public String getRestriccio() {
        return restriccio;
    }

    public void setRestriccio(String restriccio) {
        this.restriccio = restriccio;
    }

    public String getPeriode() {
        return periode;
    }

    public void setPeriode(String periode) {
        this.periode = periode;
    }

    public String getItinerari() {
        return itinerari;
    }

    public void setItinerari(String itinerari) {
        this.itinerari = itinerari;
    }

    public String getVehicle() {
        return vehicle;
    }

    public void setVehicle(String vehicle) {
        this.vehicle = vehicle;
    }

    public String getTemps_itinerari() {
        return temps_itinerari;
    }

    public void setTemps_itinerari(String temps_itinerari) {
        this.temps_itinerari = temps_itinerari;
    }

    public String getTrajecte() {
        return trajecte;
    }

    public void setTrajecte(String trajecte) {
        this.trajecte = trajecte;
    }
}
